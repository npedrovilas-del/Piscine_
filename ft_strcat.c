char	*ft_strcat(char *dest, char *src)
{
	int	j;
	int	c;

	c = 0;
	j = 0;
	while (dest[j] != '\0')
	{
		j++;
	}
	while (src[c] != '\0')
	{
		dest[j] = src[c];
		j++;
		c++;
	}
	dest[j] = '\0';
	return (dest);
}
