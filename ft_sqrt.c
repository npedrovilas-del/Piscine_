int	ft_sqrt(int nb)
{
	int	c;

	c = 1;
	if (nb == 0)
	{
		return (0);
	}
	if (nb < 0)
	{
		return (0);
	}
	while (nb != c * c && c < nb)
	{
		c++;
	}
	if (nb == c * c)
	{
		return (c);
	}
	return (0);
}
