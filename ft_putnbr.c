#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int	a;
	int			res;

	res = 0;
	a = nb;
	if (a < 0)
	{
		a = -a;
		write(1, "-", 1);
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
	}
	res = (a % 10) + '0';
	write (1, &res, 1);
}
